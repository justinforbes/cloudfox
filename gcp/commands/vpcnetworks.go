// generateVPCNetworksDiagram creates an ASCII visualization of VPC networks
func (m *VPCNetworksModule) generateVPCNetworksDiagram() string {
	allNetworks := m.getAllNetworks()
	if len(allNetworks) == 0 {
		return ""
	}

	// Convert networks to diagram service types
	diagramNetworks := make([]diagramservice.NetworkInfo, 0, len(allNetworks))
	for _, n := range allNetworks {
		diagramNetworks = append(diagramNetworks, diagramservice.NetworkInfo{
			Name:          n.Name,
			ProjectID:     n.ProjectID,
			RoutingMode:   n.RoutingMode,
			MTU:           0, // VPCNetworkInfo doesn't have MTU
			IsSharedVPC:   false,
			SharedVPCRole: "",
			PeeringCount:  len(n.Peerings),
		})
	}

	// Build subnets by network
	subnetsByNetwork := make(map[string][]diagramservice.SubnetInfo)
	for _, s := range m.getAllSubnets() {
		key := s.ProjectID + "/" + s.Network
		subnetsByNetwork[key] = append(subnetsByNetwork[key], diagramservice.SubnetInfo{
			Name:                  s.Name,
			Region:                s.Region,
			IPCIDRRange:           s.IPCidrRange,
			PrivateIPGoogleAccess: s.PrivateIPGoogleAccess,
			FlowLogsEnabled:       s.EnableFlowLogs,
		})
	}

	// Convert peerings to diagram service types
	diagramPeerings := make([]diagramservice.VPCPeeringInfo, 0, len(m.getAllPeerings()))
	for _, p := range m.getAllPeerings() {
		diagramPeerings = append(diagramPeerings, diagramservice.VPCPeeringInfo{
			Name:          p.Name,
			Network:       p.Network,
			PeerNetwork:   p.PeerNetwork,
			PeerProjectID: p.PeerProjectID,
			State:         p.State,
			ExportRoutes:  p.ExportCustomRoutes,
			ImportRoutes:  p.ImportCustomRoutes,
		})
	}

	// Determine project ID for header (use first project if multiple)
	projectID := ""
	if len(m.ProjectIDs) == 1 {
		projectID = m.ProjectIDs[0]
	}

	return diagramservice.DrawVPCNetworksDiagram(diagramNetworks, subnetsByNetwork, diagramPeerings, projectID, 90)
}

func (m *VPCNetworksModule) getNetworksHeader() []string {
	return []string{"Project", "Name", "Routing Mode", "Auto Subnets", "Subnets", "Peerings"}
}

func (m *VPCNetworksModule) getSubnetsHeader() []string {
	return []string{"Project", "Name", "Network", "Region", "CIDR", "Private Access", "Flow Logs"}
}

func (m *VPCNetworksModule) getPeeringsHeader() []string {
	return []string{"Project", "Name", "Network", "Peer Network", "Peer Project", "State", "Export Routes", "Import Routes"}
}

func (m *VPCNetworksModule) getRoutesHeader() []string {
	return []string{"Project", "Name", "Network", "Dest Range", "Next Hop Type", "Next Hop", "Priority"}
}

func (m *VPCNetworksModule) networksToTableBody(networks []vpcservice.VPCNetworkInfo) [][]string {
	var body [][]string
	for _, network := range networks {
		autoSubnets := "No"
		if network.AutoCreateSubnetworks {
			autoSubnets = "Yes"
		}
		body = append(body, []string{
			m.GetProjectName(network.ProjectID),
			network.Name,
			network.RoutingMode,
			autoSubnets,
			fmt.Sprintf("%d", len(network.Subnetworks)),
			fmt.Sprintf("%d", len(network.Peerings)),
		})
	}
	return body
}

func (m *VPCNetworksModule) subnetsToTableBody(subnets []vpcservice.SubnetInfo) [][]string {
	var body [][]string
	for _, subnet := range subnets {
		privateAccess := "No"
		if subnet.PrivateIPGoogleAccess {
			privateAccess = "Yes"
		}
		flowLogs := "No"
		if subnet.EnableFlowLogs {
			flowLogs = "Yes"
		}
		body = append(body, []string{
			m.GetProjectName(subnet.ProjectID),
			subnet.Name,
			subnet.Network,
			subnet.Region,
			subnet.IPCidrRange,
			privateAccess,
			flowLogs,
		})
	}
	return body
}

func (m *VPCNetworksModule) peeringsToTableBody(peerings []vpcservice.VPCPeeringInfo) [][]string {
	var body [][]string
	for _, peering := range peerings {
		peerProject := peering.PeerProjectID
		if peerProject == "" {
			peerProject = "-"
		}
		exportRoutes := "No"
		if peering.ExportCustomRoutes {
			exportRoutes = "Yes"
		}
		importRoutes := "No"
		if peering.ImportCustomRoutes {
			importRoutes = "Yes"
		}
		body = append(body, []string{
			m.GetProjectName(peering.ProjectID),
			peering.Name,
			peering.Network,
			peering.PeerNetwork,
			peerProject,
			peering.State,
			exportRoutes,
			importRoutes,
		})
	}
	return body
}

func (m *VPCNetworksModule) routesToTableBody(routes []vpcservice.RouteInfo) [][]string {
	var body [][]string
	// Filter to custom routes only
	for _, route := range routes {
		if strings.HasPrefix(route.Name, "default-route-") {
			continue
		}
		body = append(body, []string{
			m.GetProjectName(route.ProjectID),
			route.Name,
			route.Network,
			route.DestRange,
			route.NextHopType,
			route.NextHop,
			fmt.Sprintf("%d", route.Priority),
		})
	}
	return body
}

func (m *VPCNetworksModule) buildTablesForProject(projectID string) []internal.TableFile {
	var tableFiles []internal.TableFile

	if networks, ok := m.ProjectNetworks[projectID]; ok && len(networks) > 0 {
		tableFiles = append(tableFiles, internal.TableFile{
			Name:   "vpc-networks",
			Header: m.getNetworksHeader(),
			Body:   m.networksToTableBody(networks),
		})
	}

	if subnets, ok := m.ProjectSubnets[projectID]; ok && len(subnets) > 0 {
		tableFiles = append(tableFiles, internal.TableFile{
			Name:   "subnets",
			Header: m.getSubnetsHeader(),
			Body:   m.subnetsToTableBody(subnets),
		})
	}

	if peerings, ok := m.ProjectPeerings[projectID]; ok && len(peerings) > 0 {
		tableFiles = append(tableFiles, internal.TableFile{
			Name:   "vpc-peerings",
			Header: m.getPeeringsHeader(),
			Body:   m.peeringsToTableBody(peerings),
		})
	}

	if routes, ok := m.ProjectRoutes[projectID]; ok && len(routes) > 0 {
		routeBody := m.routesToTableBody(routes)
		if len(routeBody) > 0 {
			tableFiles = append(tableFiles, internal.TableFile{
				Name:   "custom-routes",
				Header: m.getRoutesHeader(),
				Body:   routeBody,
			})
		}
	}

	return tableFiles
}

func (m *VPCNetworksModule) writeHierarchicalOutput(ctx context.Context, logger internal.Logger) {
	outputData := internal.HierarchicalOutputData{
		OrgLevelData:     make(map[string]internal.CloudfoxOutput),
		ProjectLevelData: make(map[string]internal.CloudfoxOutput),
	}

	// Get all project IDs that have data
	projectIDs := make(map[string]bool)
	for projectID := range m.ProjectNetworks {
		projectIDs[projectID] = true
	}
	for projectID := range m.ProjectSubnets {
		projectIDs[projectID] = true
	}
	for projectID := range m.ProjectPeerings {
		projectIDs[projectID] = true
	}
	for projectID := range m.ProjectRoutes {
		projectIDs[projectID] = true
	}

	for projectID := range projectIDs {
		tableFiles := m.buildTablesForProject(projectID)

		var lootFiles []internal.LootFile
		if projectLoot, ok := m.LootMap[projectID]; ok {
			for _, loot := range projectLoot {
				if loot != nil && loot.Contents != "" && !strings.HasSuffix(loot.Contents, "# WARNING: Only use with proper authorization\n\n") {
					lootFiles = append(lootFiles, *loot)
				}
			}
		}

		outputData.ProjectLevelData[projectID] = VPCNetworksOutput{Table: tableFiles, Loot: lootFiles}
	}

	pathBuilder := m.BuildPathBuilder()

	err := internal.HandleHierarchicalOutputSmart("gcp", m.Format, m.Verbosity, m.WrapTable, pathBuilder, outputData)
	if err != nil {
		logger.ErrorM(fmt.Sprintf("Error writing hierarchical output: %v", err), globals.GCP_VPCNETWORKS_MODULE_NAME)
	}
}

func (m *VPCNetworksModule) writeFlatOutput(ctx context.Context, logger internal.Logger) {
	allNetworks := m.getAllNetworks()
	allSubnets := m.getAllSubnets()
	allPeerings := m.getAllPeerings()
	allRoutes := m.getAllRoutes()

	var tables []internal.TableFile

	if len(allNetworks) > 0 {
		tables = append(tables, internal.TableFile{
			Name:   "vpc-networks",
			Header: m.getNetworksHeader(),
			Body:   m.networksToTableBody(allNetworks),
		})
	}

	if len(allSubnets) > 0 {
		tables = append(tables, internal.TableFile{
			Name:   "subnets",
			Header: m.getSubnetsHeader(),
			Body:   m.subnetsToTableBody(allSubnets),
		})
	}

	if len(allPeerings) > 0 {
		tables = append(tables, internal.TableFile{
			Name:   "vpc-peerings",
			Header: m.getPeeringsHeader(),
			Body:   m.peeringsToTableBody(allPeerings),
		})
	}

	routeBody := m.routesToTableBody(allRoutes)
	if len(routeBody) > 0 {
		tables = append(tables, internal.TableFile{
			Name:   "custom-routes",
			Header: m.getRoutesHeader(),
			Body:   routeBody,
		})
	}

	var lootFiles []internal.LootFile
	for _, projectLoot := range m.LootMap {
		for _, loot := range projectLoot {
			if loot != nil && loot.Contents != "" && !strings.HasSuffix(loot.Contents, "# WARNING: Only use with proper authorization\n\n") {
				lootFiles = append(lootFiles, *loot)
			}
		}
	}

	output := VPCNetworksOutput{Table: tables, Loot: lootFiles}

	scopeNames := make([]string, len(m.ProjectIDs))
	for i, id := range m.ProjectIDs {
		scopeNames[i] = m.GetProjectName(id)
	}

	err := internal.HandleOutputSmart("gcp", m.Format, m.OutputDirectory, m.Verbosity, m.WrapTable,
		"project", m.ProjectIDs, scopeNames, m.Account, output)
	if err != nil {
		logger.ErrorM(fmt.Sprintf("Error writing output: %v", err), globals.GCP_VPCNETWORKS_MODULE_NAME)
	}
}
