func (m *NotebooksModule) instancesToTableBody(instances []notebooksservice.NotebookInstanceInfo) [][]string {
	var body [][]string
	for _, instance := range instances {
		gpu := "-"
		if instance.AcceleratorCount > 0 {
			gpu = fmt.Sprintf("%s x%d", instance.AcceleratorType, instance.AcceleratorCount)
		}
		sa := instance.ServiceAccount
		if sa == "" {
			sa = "(default)"
		}

		// Check attack paths (privesc/exfil/lateral) for the service account
		attackPaths := "-"
		if m.AttackPathCache != nil && m.AttackPathCache.IsPopulated() {
			if sa != "(default)" && sa != "" {
				attackPaths = m.AttackPathCache.GetAttackSummary(sa)
			} else {
				attackPaths = "No"
			}
		}

		network := instance.Network
		if network == "" {
			network = "-"
		}
		subnet := instance.Subnet
		if subnet == "" {
			subnet = "-"
		}
		proxyUri := instance.ProxyUri
		if proxyUri == "" {
			proxyUri = "-"
		}
		creator := instance.Creator
		if creator == "" {
			creator = "-"
		}
		body = append(body, []string{
			m.GetProjectName(instance.ProjectID),
			instance.ProjectID,
			instance.Name,
			instance.Location,
			instance.State,
			instance.MachineType,
			sa,
			attackPaths,
			network,
			subnet,
			boolToYesNo(!instance.NoPublicIP),
			boolToYesNo(!instance.NoProxyAccess),
			proxyUri,
			gpu,
			creator,
		})
	}
	return body
}

