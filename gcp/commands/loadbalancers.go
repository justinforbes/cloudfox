var GCPLoadBalancersCommand = &cobra.Command{
	Use:     globals.GCP_LOADBALANCERS_MODULE_NAME,
	Aliases: []string{"lb", "lbs"},
	Short:   "Enumerate Load Balancers",
	Long: `Enumerate Load Balancers and related configurations.

Features:
- Lists all forwarding rules (global and regional)
- Shows backend services and health checks
- Analyzes SSL policies for weak configurations
- Identifies external vs internal load balancers
- Checks for Cloud Armor security policies`,
	Run: runGCPLoadBalancersCommand,
}

type LoadBalancersModule struct {
	gcpinternal.BaseGCPModule
	ProjectLoadBalancers   map[string][]loadbalancerservice.LoadBalancerInfo   // projectID -> load balancers
	ProjectSSLPolicies     map[string][]loadbalancerservice.SSLPolicyInfo      // projectID -> SSL policies
	ProjectBackendServices map[string][]loadbalancerservice.BackendServiceInfo // projectID -> backend services
	LootMap                map[string]map[string]*internal.LootFile            // projectID -> loot files
	mu                     sync.Mutex
}

type LoadBalancersOutput struct {
	Table []internal.TableFile
	Loot  []internal.LootFile
}

func (o LoadBalancersOutput) TableFiles() []internal.TableFile { return o.Table }
func (o LoadBalancersOutput) LootFiles() []internal.LootFile   { return o.Loot }

func runGCPLoadBalancersCommand(cmd *cobra.Command, args []string) {
	cmdCtx, err := gcpinternal.InitializeCommandContext(cmd, globals.GCP_LOADBALANCERS_MODULE_NAME)
	if err != nil {
		return
	}

	module := &LoadBalancersModule{
		BaseGCPModule:          gcpinternal.NewBaseGCPModule(cmdCtx),
		ProjectLoadBalancers:   make(map[string][]loadbalancerservice.LoadBalancerInfo),
		ProjectSSLPolicies:     make(map[string][]loadbalancerservice.SSLPolicyInfo),
		ProjectBackendServices: make(map[string][]loadbalancerservice.BackendServiceInfo),
		LootMap:                make(map[string]map[string]*internal.LootFile),
	}
	module.Execute(cmdCtx.Ctx, cmdCtx.Logger)
}

func (m *LoadBalancersModule) Execute(ctx context.Context, logger internal.Logger) {
	m.RunProjectEnumeration(ctx, logger, m.ProjectIDs, globals.GCP_LOADBALANCERS_MODULE_NAME, m.processProject)

	allLoadBalancers := m.getAllLoadBalancers()
	allSSLPolicies := m.getAllSSLPolicies()
	allBackendServices := m.getAllBackendServices()

	if len(allLoadBalancers) == 0 {
		logger.InfoM("No load balancers found", globals.GCP_LOADBALANCERS_MODULE_NAME)
		return
	}

	externalCount := 0
	for _, lb := range allLoadBalancers {
		if lb.Scheme == "EXTERNAL" {
			externalCount++
		}
	}

	logger.SuccessM(fmt.Sprintf("Found %d load balancer(s) (%d external), %d SSL policies, %d backend services",
		len(allLoadBalancers), externalCount, len(allSSLPolicies), len(allBackendServices)), globals.GCP_LOADBALANCERS_MODULE_NAME)
	m.writeOutput(ctx, logger)
}

func (m *LoadBalancersModule) getAllLoadBalancers() []loadbalancerservice.LoadBalancerInfo {
	var all []loadbalancerservice.LoadBalancerInfo
	for _, lbs := range m.ProjectLoadBalancers {
		all = append(all, lbs...)
	}
	return all
}

func (m *LoadBalancersModule) getAllSSLPolicies() []loadbalancerservice.SSLPolicyInfo {
	var all []loadbalancerservice.SSLPolicyInfo
	for _, policies := range m.ProjectSSLPolicies {
		all = append(all, policies...)
	}
	return all
}

func (m *LoadBalancersModule) getAllBackendServices() []loadbalancerservice.BackendServiceInfo {
	var all []loadbalancerservice.BackendServiceInfo
	for _, services := range m.ProjectBackendServices {
		all = append(all, services...)
	}
	return all
}

func (m *LoadBalancersModule) processProject(ctx context.Context, projectID string, logger internal.Logger) {
	if globals.GCP_VERBOSITY >= globals.GCP_VERBOSE_ERRORS {
		logger.InfoM(fmt.Sprintf("Enumerating load balancers in project: %s", projectID), globals.GCP_LOADBALANCERS_MODULE_NAME)
	}

	svc := loadbalancerservice.New()

	m.mu.Lock()
	// Initialize loot for this project
	if m.LootMap[projectID] == nil {
		m.LootMap[projectID] = make(map[string]*internal.LootFile)
		m.LootMap[projectID]["loadbalancers-commands"] = &internal.LootFile{
			Name:     "loadbalancers-commands",
			Contents: "# Load Balancer Commands\n# Generated by CloudFox\n# WARNING: Only use with proper authorization\n\n",
		}
	}
	m.mu.Unlock()

	// Get load balancers
	lbs, err := svc.ListLoadBalancers(projectID)
	if err != nil {
		m.CommandCounter.Error++
		gcpinternal.HandleGCPError(err, logger, globals.GCP_LOADBALANCERS_MODULE_NAME,
			fmt.Sprintf("Could not list load balancers in project %s", projectID))
	} else {
		m.mu.Lock()
		m.ProjectLoadBalancers[projectID] = lbs
		for _, lb := range lbs {
			m.addToLoot(projectID, lb)
		}
		m.mu.Unlock()
	}

	// Get SSL policies
	sslPolicies, err := svc.ListSSLPolicies(projectID)
	if err != nil {
		m.CommandCounter.Error++
		gcpinternal.HandleGCPError(err, logger, globals.GCP_LOADBALANCERS_MODULE_NAME,
			fmt.Sprintf("Could not list SSL policies in project %s", projectID))
	} else {
		m.mu.Lock()
		m.ProjectSSLPolicies[projectID] = sslPolicies
		m.mu.Unlock()
	}

	// Get backend services
	backends, err := svc.ListBackendServices(projectID)
	if err != nil {
		m.CommandCounter.Error++
		gcpinternal.HandleGCPError(err, logger, globals.GCP_LOADBALANCERS_MODULE_NAME,
			fmt.Sprintf("Could not list backend services in project %s", projectID))
	} else {
		m.mu.Lock()
		m.ProjectBackendServices[projectID] = backends
		m.mu.Unlock()
	}
}

func (m *LoadBalancersModule) addToLoot(projectID string, lb loadbalancerservice.LoadBalancerInfo) {
	lootFile := m.LootMap[projectID]["loadbalancers-commands"]
	if lootFile == nil {
		return
	}
	lootFile.Contents += fmt.Sprintf(
		"# =============================================================================\n"+
			"# LOAD BALANCER: %s\n"+
			"# =============================================================================\n"+
			"# Project: %s\n"+
			"# Type: %s, Scheme: %s, IP: %s, Port: %s\n\n",
		lb.Name, lb.ProjectID, lb.Type, lb.Scheme, lb.IPAddress, lb.Port)

	lootFile.Contents += "# === ENUMERATION COMMANDS ===\n\n"

	// Describe forwarding rule
	if lb.Region == "global" {
		lootFile.Contents += fmt.Sprintf(
			"# Describe global forwarding rule:\n"+
				"gcloud compute forwarding-rules describe %s --global --project=%s\n\n",
			lb.Name, lb.ProjectID)
	} else {
		lootFile.Contents += fmt.Sprintf(
			"# Describe regional forwarding rule:\n"+
				"gcloud compute forwarding-rules describe %s --region=%s --project=%s\n\n",
			lb.Name, lb.Region, lb.ProjectID)
	}

	// Backend service commands
	for _, backend := range lb.BackendServices {
		lootFile.Contents += fmt.Sprintf(
			"# Describe backend service:\n"+
				"gcloud compute backend-services describe %s --global --project=%s\n\n",
			backend, lb.ProjectID)
	}
}

func (m *LoadBalancersModule) writeOutput(ctx context.Context, logger internal.Logger) {
	// Generate ASCII diagram and add to loot
	diagram := m.generateLoadBalancerDiagram()
	if diagram != "" {
		// Add diagram to the first project's loot
		for projectID := range m.LootMap {
			if m.LootMap[projectID] == nil {
				m.LootMap[projectID] = make(map[string]*internal.LootFile)
			}
			m.LootMap[projectID]["loadbalancers-diagram"] = &internal.LootFile{
				Name:     "loadbalancers-diagram",
				Contents: diagram,
			}
			break // Only add once for flat output
		}

		// For hierarchical output, add to all projects
		if m.Hierarchy != nil && !m.FlatOutput {
			for projectID := range m.LootMap {
				if m.LootMap[projectID] == nil {
					m.LootMap[projectID] = make(map[string]*internal.LootFile)
				}
				m.LootMap[projectID]["loadbalancers-diagram"] = &internal.LootFile{
					Name:     "loadbalancers-diagram",
					Contents: diagram,
				}
			}
		}
	}

	if m.Hierarchy != nil && !m.FlatOutput {
		m.writeHierarchicalOutput(ctx, logger)
	} else {
		m.writeFlatOutput(ctx, logger)
	}
}

