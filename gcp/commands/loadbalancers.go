// generateLoadBalancerDiagram creates an ASCII visualization of load balancer architecture
func (m *LoadBalancersModule) generateLoadBalancerDiagram() string {
	allLBs := m.getAllLoadBalancers()
	if len(allLBs) == 0 {
		return ""
	}

	// Build a map of backend service name -> actual backends (instance groups, NEGs)
	backendDetailsMap := make(map[string][]string)
	for _, backends := range m.ProjectBackendServices {
		for _, be := range backends {
			if len(be.Backends) > 0 {
				backendDetailsMap[be.Name] = be.Backends
			}
		}
	}

	// Convert to diagram service types
	diagramLBs := make([]diagramservice.LoadBalancerInfo, 0, len(allLBs))
	for _, lb := range allLBs {
		// Build backend details for this LB
		lbBackendDetails := make(map[string][]string)
		for _, beSvc := range lb.BackendServices {
			if targets, ok := backendDetailsMap[beSvc]; ok {
				lbBackendDetails[beSvc] = targets
			}
		}

		diagramLBs = append(diagramLBs, diagramservice.LoadBalancerInfo{
			Name:            lb.Name,
			Type:            lb.Type,
			Scheme:          lb.Scheme,
			IPAddress:       lb.IPAddress,
			Port:            lb.Port,
			Region:          lb.Region,
			BackendServices: lb.BackendServices,
			SecurityPolicy:  lb.SecurityPolicy,
			BackendDetails:  lbBackendDetails,
		})
	}

	// Determine project ID for header (use first project if multiple)
	projectID := ""
	if len(m.ProjectIDs) == 1 {
		projectID = m.ProjectIDs[0]
	}

	return diagramservice.DrawLoadBalancerDiagram(diagramLBs, projectID, 90)
}

func (m *LoadBalancersModule) getLBHeader() []string {
	return []string{"Project", "Name", "Type", "Scheme", "Region", "IP Address", "Port", "Backend Services", "Security Policy"}
}

func (m *LoadBalancersModule) getSSLHeader() []string {
	return []string{"Project", "Name", "Min TLS Version", "Profile", "Custom Features"}
}

func (m *LoadBalancersModule) getBackendHeader() []string {
	return []string{"Project", "Name", "Protocol", "Port", "Security Policy", "CDN Enabled", "Health Check", "Session Affinity", "Backends"}
}

func (m *LoadBalancersModule) lbsToTableBody(lbs []loadbalancerservice.LoadBalancerInfo) [][]string {
	var body [][]string
	for _, lb := range lbs {
		backends := "-"
		if len(lb.BackendServices) > 0 {
			backends = strings.Join(lb.BackendServices, ", ")
		}
		secPolicy := "-"
		if lb.SecurityPolicy != "" {
			secPolicy = lb.SecurityPolicy
		}
		body = append(body, []string{
			m.GetProjectName(lb.ProjectID),
			lb.Name,
			lb.Type,
			lb.Scheme,
			lb.Region,
			lb.IPAddress,
			lb.Port,
			backends,
			secPolicy,
		})
	}
	return body
}

func (m *LoadBalancersModule) sslPoliciesToTableBody(policies []loadbalancerservice.SSLPolicyInfo) [][]string {
	var body [][]string
	for _, policy := range policies {
		customFeatures := "-"
		if len(policy.CustomFeatures) > 0 {
			customFeatures = strings.Join(policy.CustomFeatures, ", ")
		}
		body = append(body, []string{
			m.GetProjectName(policy.ProjectID),
			policy.Name,
			policy.MinTLSVersion,
			policy.Profile,
			customFeatures,
		})
	}
	return body
}

func (m *LoadBalancersModule) backendServicesToTableBody(services []loadbalancerservice.BackendServiceInfo) [][]string {
	var body [][]string
	for _, be := range services {
		secPolicy := "-"
		if be.SecurityPolicy != "" {
			secPolicy = be.SecurityPolicy
		}
		healthCheck := "-"
		if be.HealthCheck != "" {
			healthCheck = be.HealthCheck
		}
		sessionAffinity := "-"
		if be.SessionAffinity != "" {
			sessionAffinity = be.SessionAffinity
		}
		backends := "-"
		if len(be.Backends) > 0 {
			backends = strings.Join(be.Backends, ", ")
		}
		body = append(body, []string{
			m.GetProjectName(be.ProjectID),
			be.Name,
			be.Protocol,
			fmt.Sprintf("%d", be.Port),
			secPolicy,
			shared.BoolToYesNo(be.EnableCDN),
			healthCheck,
			sessionAffinity,
			backends,
		})
	}
	return body
}

func (m *LoadBalancersModule) buildTablesForProject(projectID string) []internal.TableFile {
	var tableFiles []internal.TableFile

	if lbs, ok := m.ProjectLoadBalancers[projectID]; ok && len(lbs) > 0 {
		tableFiles = append(tableFiles, internal.TableFile{
			Name:   "load-balancers-frontends",
			Header: m.getLBHeader(),
			Body:   m.lbsToTableBody(lbs),
		})
	}

	if policies, ok := m.ProjectSSLPolicies[projectID]; ok && len(policies) > 0 {
		tableFiles = append(tableFiles, internal.TableFile{
			Name:   "ssl-policies",
			Header: m.getSSLHeader(),
			Body:   m.sslPoliciesToTableBody(policies),
		})
	}

	if services, ok := m.ProjectBackendServices[projectID]; ok && len(services) > 0 {
		tableFiles = append(tableFiles, internal.TableFile{
			Name:   "load-balancers-backend-services",
			Header: m.getBackendHeader(),
			Body:   m.backendServicesToTableBody(services),
		})
	}

	return tableFiles
}

func (m *LoadBalancersModule) writeHierarchicalOutput(ctx context.Context, logger internal.Logger) {
	outputData := internal.HierarchicalOutputData{
		OrgLevelData:     make(map[string]internal.CloudfoxOutput),
		ProjectLevelData: make(map[string]internal.CloudfoxOutput),
	}

	// Get all project IDs that have data
	projectIDs := make(map[string]bool)
	for projectID := range m.ProjectLoadBalancers {
		projectIDs[projectID] = true
	}
	for projectID := range m.ProjectSSLPolicies {
		projectIDs[projectID] = true
	}
	for projectID := range m.ProjectBackendServices {
		projectIDs[projectID] = true
	}

	for projectID := range projectIDs {
		tableFiles := m.buildTablesForProject(projectID)

		var lootFiles []internal.LootFile
		if projectLoot, ok := m.LootMap[projectID]; ok {
			for _, loot := range projectLoot {
				if loot != nil && loot.Contents != "" && !strings.HasSuffix(loot.Contents, "# WARNING: Only use with proper authorization\n\n") {
					lootFiles = append(lootFiles, *loot)
				}
			}
		}

		outputData.ProjectLevelData[projectID] = LoadBalancersOutput{Table: tableFiles, Loot: lootFiles}
	}

	pathBuilder := m.BuildPathBuilder()

	err := internal.HandleHierarchicalOutputSmart("gcp", m.Format, m.Verbosity, m.WrapTable, pathBuilder, outputData)
	if err != nil {
		logger.ErrorM(fmt.Sprintf("Error writing hierarchical output: %v", err), globals.GCP_LOADBALANCERS_MODULE_NAME)
	}
}

func (m *LoadBalancersModule) writeFlatOutput(ctx context.Context, logger internal.Logger) {
	allLBs := m.getAllLoadBalancers()
	allSSL := m.getAllSSLPolicies()
	allBackends := m.getAllBackendServices()

	var tables []internal.TableFile

	if len(allLBs) > 0 {
		tables = append(tables, internal.TableFile{
			Name:   "load-balancers-frontends",
			Header: m.getLBHeader(),
			Body:   m.lbsToTableBody(allLBs),
		})
	}

	if len(allSSL) > 0 {
		tables = append(tables, internal.TableFile{
			Name:   "ssl-policies",
			Header: m.getSSLHeader(),
			Body:   m.sslPoliciesToTableBody(allSSL),
		})
	}

	if len(allBackends) > 0 {
		tables = append(tables, internal.TableFile{
			Name:   "load-balancers-backend-services",
			Header: m.getBackendHeader(),
			Body:   m.backendServicesToTableBody(allBackends),
		})
	}

	var lootFiles []internal.LootFile
	for _, projectLoot := range m.LootMap {
		for _, loot := range projectLoot {
			if loot != nil && loot.Contents != "" && !strings.HasSuffix(loot.Contents, "# WARNING: Only use with proper authorization\n\n") {
				lootFiles = append(lootFiles, *loot)
			}
		}
	}

	output := LoadBalancersOutput{Table: tables, Loot: lootFiles}

	scopeNames := make([]string, len(m.ProjectIDs))
	for i, id := range m.ProjectIDs {
		scopeNames[i] = m.GetProjectName(id)
	}

	err := internal.HandleOutputSmart("gcp", m.Format, m.OutputDirectory, m.Verbosity, m.WrapTable,
		"project", m.ProjectIDs, scopeNames, m.Account, output)
	if err != nil {
		logger.ErrorM(fmt.Sprintf("Error writing output: %v", err), globals.GCP_LOADBALANCERS_MODULE_NAME)
	}
}
